    def paste_in_radios_table(self):
        columns = 'id, name, url, is_play'
        data = extract_radios_data(columns)
        self.tableWidget.setRowCount(len(data))
        if data:
            row = 0
            for radio in data:
                self.tableWidget.setItem(row, 0, QtWidgets.QTableWidgetItem(radio[1].title()))
                self.tableWidget.setItem(row, 1, QtWidgets.QTableWidgetItem(radio[2]))
                row += 1
                if radio[3]:
                    self.radio_current_url = radio[2]
        self.lineEdit_new_station_url.clear()
        self.lineEdit_new_station_name.clear()

